/* MATH */

// Alguns exemplos do uso do Math

console.log(Math.PI) // Mostra o Valor de PI.
console.log(Math.pow(2,3)) // Permite usarmos potenciação.
console.log(Math.sqrt(9,2)) // Permite calcularmos a raiz de um número.
console.log(Math.random()) // Mostra um número aleatório entre 0 e 1.
console.log(Math.ceil(23.6)) // Arredonda um número para CIMA.
console.log(Math.floor(23.6)) // Arredonda um número para BAIXO.